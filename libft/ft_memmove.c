#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t n)
{
	char	*m;
	char	*t;

	m = (char *) dst;
	t = (char *) src;
	if (t >= m)
		ft_memcpy(dst, src, n);
	else
	{
		while (n > 0)
		{
			n--;
			m[n] = t[n];
		}
	}
	return (dst);
}
