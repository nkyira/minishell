#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;

	temp = *lst;
	while (temp)
	{	
		*lst = (*lst)->next;
		ft_lstdelone(temp, del);
		temp = *lst;
	}
}
